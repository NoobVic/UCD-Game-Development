	// This is the heart of the game , where the model takes in all the inputs ,decides the outcomes and then changes the model accordingly. 
	public void gamelogic()
	{
			frameCounter++;
			// Player Logic first
			playerLogic();
			// Enemy Logic next
			enemyLogic();
			// Bullets move next
			bulletLogic();
			// Spells move next
			spellLogic();
			// interactions between objects
			gameLogic();
	}

	private void gameLogic() { 
		
		
		// this is a way to increment across the array list data structure 

			for (Enemy temp : EnemiesList)
			{
				for (Bullet bullet : BulletList)
				{
					if ( Math.abs(temp.getCentre().getX()- bullet.getCentre().getX())< temp.getWidth() + 50
							&& Math.abs(temp.getCentre().getY()- bullet.getCentre().getY() - 20) < temp.getHeight())
					{
						int dmg = ADdamageCalculator(temp, bullet);
						if(deathDetect(temp, dmg)){
							EnemiesList.remove(temp);
							BulletList.remove(bullet);
							// gain exp
							player.setExp(player.getExp() + temp.getExp());
							if( player.levelUpCheck() ) {
								levelUp(player);
								System.out.println(player.toString());
							}
						} else {
							Information tempInfo = temp.getInfo();
							int tempHP = tempInfo.getHP();
							tempInfo.setHP(tempHP - dmg);
							temp.getCentre().ApplyVector(new Vector3f(20,0,0));
							BulletList.remove(bullet);
						}
					}
				}

				for (Spell spell : SpellList)
				{
					if ( Math.abs(temp.getCentre().getX()- spell.getCentre().getX())< temp.getWidth() + 50
							&& Math.abs(temp.getCentre().getY()- spell.getCentre().getY() - 20) < temp.getHeight())
					{
						int dmg = APdamageCalculator(temp, spell);
						if(deathDetect(temp, dmg)){
							EnemiesList.remove(temp);
							player.setExp(player.getExp() + temp.getExp());
							if( player.levelUpCheck() ) {
								levelUp(player);
								System.out.println(player.toString());
							}
						} else {
							Information tempInfo = temp.getInfo();
							int tempHP = tempInfo.getHP();
							tempInfo.setHP(tempHP - dmg);
							temp.getCentre().ApplyVector(new Vector3f(20,0,0));
							SpellList.remove(spell);
						}
					}
				}

				if ( Math.abs(temp.getCentre().getX()- player.getCentre().getX())< temp.getWidth() + 50
						&& Math.abs(temp.getCentre().getY()- player.getCentre().getY() - 20) < temp.getHeight())
				{
					Bullet enemyAttack = new Bullet(null,0,0,null, temp.getInfo().getAD() );
					int dmg = ADdamageCalculator(player, enemyAttack);
					if(deathDetect(player, dmg))
					{
						setPlayerDead(true);
						break;
					}else {
						player.getInfo().setHP(player.getInfo().getHP()-dmg);
					}
				}

		//see if they hit anything 
		// using enhanced for-loop style as it makes it alot easier both code wise and reading wise too 

		}
	}

