	private void levelUp(Player player){
		player.levelUp();
		setLevelUp(true);
	}
	private boolean deathDetect(GameObject target, int dmg){
		return target.getInfo().getHP() - dmg <= 0;
	}

	private int ADdamageCalculator(GameObject target, Bullet bullet) {
		int ar = target.getInfo().getAR();
		int dmg = bullet.getDamage();
		return (int) Math.round(100.0/(100+ar) * dmg);
	}

	private int APdamageCalculator(GameObject target, Spell spell) {
		int mr = target.getInfo().getAR();
		int dmg = spell.getDamage();
		return (int) Math.round(100.0/(100+mr) * dmg);
	}

	private void enemyLogic() {
		// TODO Auto-generated method stub
		for (Enemy temp : EnemiesList)
		{
		    // Move enemies 
			Point3f playerPosition = player.getCentre();
			Point3f enmemyPosition = temp.getCentre();
			float x_diff = playerPosition.getX()+50 - enmemyPosition.getX();
			float y_diff = playerPosition.getY()+50 - enmemyPosition.getY();
			if(x_diff != 0 ){
				temp.getCentre().ApplyVector(new Vector3f((float) 0.5* x_diff/Math.abs(x_diff),0,0));
			}
			if(y_diff != 0){
				temp.getCentre().ApplyVector(new Vector3f(0,(float) 0.1*-y_diff/Math.abs(y_diff),0));
			}
		}

		if(EnemiesList.size() == 0){
			stageFinished=true;
			if(stageCounter==6) { gameover=true; }
		}

		if(stageFinished & !gameover) {
			stageFinished=false;
			if(stageCounter<5) {
				// stage list
				HashMap<Integer, Enemy> enemies = stages.getStages()[stageCounter].getEnemies();
				for (Enemy enemy : enemies.values()) {
					EnemiesList.add(enemy);
				}
				stageCounter++;
			} else {
				//Boss stage
				EnemiesList.add(new Enemy(EnemyTypes.slimeKing, new Point3f(900,400,0)));
				stageCounter++;
			}
		}
	}

	private void bulletLogic() {
		// TODO Auto-generated method stub
		// move bullets 
	  
		for (Bullet temp : BulletList)
		{
		    //check to move them
			  
			temp.getCentre().ApplyVector(new Vector3f(1,0,0));
			//see if they hit anything 
			
			//see if they get to the top of the screen ( remember 0 is the top 
			if (temp.getCentre().getX()==900.0f)
			{
			 	BulletList.remove(temp);
			} 
		} 
		
	}

	private void spellLogic() {
		// TODO Auto-generated method stub
		// move bullets

		for (Spell temp : SpellList)
		{
			//check to move them

			temp.getCentre().ApplyVector(new Vector3f(1,0,0));
			//see if they hit anything

			//see if they get to the top of the screen ( remember 0 is the top
			if (temp.getCentre().getX()==900.0f)
			{
				SpellList.remove(temp);
			}
		}

	}

	private void playerLogic() {
		
		// smoother animation is possible if we make a target position  // done but may try to change things for students  
		 
		//check for movement and if you fired a bullet 
		  
		if(Controller.getInstance().isKeyAPressed()){player.getCentre().ApplyVector( new Vector3f(-2,0,0)); }

		if(Controller.getInstance().isKeyDPressed())
		{
			player.getCentre().ApplyVector( new Vector3f(2,0,0));
		}
			
		if(Controller.getInstance().isKeyWPressed())
		{
			player.getCentre().ApplyVector( new Vector3f(0,2,0));
		}
		
		if(Controller.getInstance().isKeySPressed()){player.getCentre().ApplyVector( new Vector3f(0,-2,0));}
		
		if(Controller.getInstance().isKeySpacePressed())
		{
			if(frameCounter > 50){
				createBullet(player);
				frameCounter = 0;
			}
			Controller.getInstance().setKeySpacePressed(false);
		}

		if(Controller.getInstance().isKeyEPressed())
		{
			Information tempInfo = player.getInfo();

			// TODO: here I just assume the damage of spell is 100 for now
			int spellDmg = 100;
			int cost = 10;
			double APratio = 0.5;
			if (tempInfo.getMP() >= cost) {
				int dmg = (int) Math.round(spellDmg + APratio * tempInfo.getAP());
				createSpell(dmg);
				tempInfo.setMP(tempInfo.getMP() - cost);
				Controller.getInstance().setKeyEPressed(false);
			}
		}
	}

	// for AD attack, the damage is just the AD value of the attack
	private void createBullet(GameObject attacker) {
		BulletList.add(new Bullet("res/Arrow.png",32,32,new Point3f(player.getCentre().getX(),player.getCentre().getY(),0.0f), attacker.getInfo().getAD() ));
	}

	// but for AP attack, the damage calculating will be more complited, so here the parameter is a calculated value
	private void createSpell(int dmg) {
		SpellList.add(new Spell("res/fireball.png",64,32,new Point3f(player.getCentre().getX(),player.getCentre().getY(),0.0f), 100 ));
	}

